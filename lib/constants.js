// Functions
export const monogramMaker = (text) => {
  return text
    .split(' ')
    .slice(0, 2)
    .map((word) => word.at(0).toUpperCase())
    .join('')
}

export function getCurrentDateTime() {
  const date = new Date()
  return date.toLocaleString('en-US', {
    weekday: 'long',
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: 'numeric',
    minute: 'numeric',
    hour12: true,
  })
}

export function calculateBasicPrice(inputStr) {
  // Extracting the number from the input string using filter function
  const numStr = inputStr.replace(/[^\d.]/g, '')

  // Converting the extracted number to a float
  const num = Math.round(parseFloat(numStr))

  // Rounding up the number, multiplying by 2, deducting 0.01
  const result = (((num * 2 - 0.01) * 100) / 100).toFixed(2)

  // Adding the currency sign back to the result
  let resultStr = ''
  if (inputStr[0] === '$') {
    resultStr = `${inputStr[0]}${result}`
  } else {
    resultStr = `${result}${inputStr.at(-1)}`
  }

  return resultStr
}
